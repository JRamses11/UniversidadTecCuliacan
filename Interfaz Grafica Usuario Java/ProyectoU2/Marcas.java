package ProyectoU2;

public class Marcas {
       private int id;
       private String nombre;
       
       public Marcas(int id, String nombre) {
    	   this.id=id;
    	   this.nombre=nombre;
       }

	public int getId() {
		return id;
	}

	public String getNombre() {
		return nombre;
	}
}
