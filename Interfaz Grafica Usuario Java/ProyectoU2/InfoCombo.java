package ProyectoU2;

public class InfoCombo {
	private int idedo,idmun,idciu;
	
	public InfoCombo(int idedo, int idmun, int idciu) {
		this.idedo=idedo;
		this.idmun=idmun;
		this.idciu=idciu;
	}

	public int getIdmun() {
		return idmun;
	}

	public int getIdciu() {
		return idciu;
	}

	public int getIdedo() {
		return idedo;
	}

}
